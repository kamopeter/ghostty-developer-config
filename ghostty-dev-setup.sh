configure_git_delta() {
  log "Configuring Git Delta"
  git config --global core.pager delta
  git config --global interactive.diffFilter "delta --color-only"
  git config --global delta.navigate true
  git config --global delta.line-numbers true
  git config --global delta.side-by-side true
  git config --global merge.conflictStyle zdiff3
}

validate_setup() {
  log "Validating configuration"

  zsh -n "$HOME/.zshrc" || die "~/.zshrc contains a syntax error."

  if command -v starship >/dev/null 2>&1; then
    STARSHIP_CONFIG="$HOME/.config/starship.toml" starship explain >/dev/null || \
      die "Starship configuration validation failed."
  fi

  echo "Validation completed."
}

initialize_atuin() {
  if command -v atuin >/dev/null 2>&1 && [[ ! -f "$HOME/.local/share/atuin/history.db" ]]; then
    log "Importing existing shell history into Atuin"
    atuin import auto || warn "Atuin history import was skipped or failed."
  fi
}

print_summary() {
  cat <<EOF

