mkcd() {
  [[ -n "\${1:-}" ]] || { echo "Usage: mkcd <directory>"; return 1; }
  mkdir -p -- "\$1" && cd -- "\$1"
}

# Keep syntax highlighting last.
source "$brew_prefix/share/zsh-syntax-highlighting/zsh-syntax-highlighting.zsh"

