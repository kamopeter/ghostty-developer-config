write_zsh_config() {
  local zshrc="$HOME/.zshrc"
  local begin_marker="# >>> GHOSTTY DEV SETUP >>>"
  local end_marker="# <<< GHOSTTY DEV SETUP <<<"
  local brew_prefix

  touch "$zshrc"
  backup_file "$zshrc"
  brew_prefix="$(brew --prefix)"

  log "Updating ~/.zshrc"

  # Remove a previous managed block, while preserving everything else.
  awk -v begin="$begin_marker" -v end="$end_marker" '
    $0 == begin { skip = 1; next }
    $0 == end   { skip = 0; next }
    !skip       { print }
  ' "$zshrc" > "$zshrc.tmp"

  mv "$zshrc.tmp" "$zshrc"

  # Disable the Angular completion line that can crash shell startup due to
  # incompatible global Angular CLI/yargs versions.
  sed -i '' -E \
    's|^[[:space:]]*(source[[:space:]]+<\(ng completion script\)|eval[[:space:]]+"\$\(ng completion script\)")|# Disabled by ghostty-dev-setup: \1|' \
    "$zshrc"

  cat >> "$zshrc" <<ZSHRC

$begin_marker

# Homebrew
eval "\$($brew_prefix/bin/brew shellenv)"

# Completion must be initialized before tools that call compdef.
autoload -Uz compinit
compinit

# Prompt and shell navigation
eval "\$(starship init zsh)"
eval "\$(atuin init zsh)"
eval "\$(zoxide init zsh)"
source <(fzf --zsh)

# Better interactive editing
source "$brew_prefix/share/zsh-autosuggestions/zsh-autosuggestions.zsh"

# History
HISTFILE="\$HOME/.zsh_history"
HISTSIZE=100000
SAVEHIST=100000
setopt APPEND_HISTORY
setopt SHARE_HISTORY
setopt HIST_IGNORE_DUPS
setopt HIST_REDUCE_BLANKS
setopt AUTO_CD
setopt CORRECT

# Modern command aliases
alias ls='eza --icons'
alias ll='eza --icons --long --git --header'
alias la='eza --icons --long --git --header --all'
alias tree='eza --tree --icons'
alias cat='bat'
alias c='clear'
alias shrc='source ~/.zshrc'
alias ezsh='open -e ~/.zshrc'
alias eghost='open -e "\$HOME/Library/Application Support/com.mitchellh.ghostty/config.ghostty"'
alias estarship='open -e ~/.config/starship.toml'

