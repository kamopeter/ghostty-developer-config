  $backup_dir

Next step:
  1. Fully quit and reopen Ghostty.
  2. Or run: exec zsh

Useful commands:
  lg         Lazygit
  Ctrl+R     Atuin history search
  z <name>   Smart directory jump
  shrc       Reload ~/.zshrc
  eghost     Edit Ghostty config
  estarship  Edit Starship config
EOF
}

main() {
  install_homebrew
  install_packages
  write_ghostty_config
  write_starship_config
  write_zsh_config
  configure_git_delta
  initialize_atuin
  validate_setup
  print_summary
}

main "$@"
