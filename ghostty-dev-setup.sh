#!/usr/bin/env bash
set -Eeuo pipefail

# Ghostty developer setup for macOS
# Safe to run repeatedly: existing configs are backed up, and the managed
# ~/.zshrc block is replaced instead of duplicated.

if [[ "$(uname -s)" != "Darwin" ]]; then
  echo "This installer is intended for macOS."
  exit 1
fi

timestamp="$(date +%Y%m%d-%H%M%S)"
backup_dir="$HOME/.config/ghostty-dev-setup/backups/$timestamp"

log() {
  printf '\n\033[1;36m==>\033[0m %s\n' "$1"
}

warn() {
  printf '\033[1;33mWarning:\033[0m %s\n' "$1" >&2
}

die() {
  printf '\033[1;31mError:\033[0m %s\n' "$1" >&2
  exit 1
}

backup_file() {
  local source_file="$1"
  if [[ -f "$source_file" ]]; then
    mkdir -p "$backup_dir"
    cp -p "$source_file" "$backup_dir/$(basename "$source_file")"
    echo "Backed up: $source_file"
  fi
}

install_homebrew() {
  if command -v brew >/dev/null 2>&1; then
    return
  fi

  log "Installing Homebrew"
  /bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"

  if [[ -x /opt/homebrew/bin/brew ]]; then
    eval "$(/opt/homebrew/bin/brew shellenv)"
  elif [[ -x /usr/local/bin/brew ]]; then
    eval "$(/usr/local/bin/brew shellenv)"
  else
    die "Homebrew installation finished, but brew was not found."
  fi
}

install_packages() {
  log "Installing command-line tools"
  brew update

  local formulae=(
    starship
    atuin
    zoxide
    fzf
    eza
    bat
    lazygit
    git-delta
    fd
    ripgrep
    zsh-autosuggestions
    zsh-syntax-highlighting
  )

  brew install "${formulae[@]}"

  log "Installing JetBrainsMono Nerd Font"
  brew install --cask font-jetbrains-mono-nerd-font || \
    warn "The Nerd Font cask could not be installed. The rest of the setup will continue."
}

write_ghostty_config() {
  local ghostty_dir="$HOME/Library/Application Support/com.mitchellh.ghostty"
  local ghostty_config="$ghostty_dir/config.ghostty"
  local old_config="$ghostty_dir/config"

  mkdir -p "$ghostty_dir"
  backup_file "$ghostty_config"
  backup_file "$old_config"

  log "Writing Ghostty configuration"

  cat > "$ghostty_config" <<'GHOSTTY'
